package Class6Operators;

public class HomeWork2Loops {
    public static void main(String[] args) {
        int i=20;
        int j=20;
        while(i<=20 && i>=1){
            System.out.print(i+" ");
            i-=2;

        }
        System.out.println();
        do{
            System.out.print(j+" ");
            j-=2;
        }while(j<=20 && j>=1);
    }
}
