package Class7NestedLoopsAndArrays;

public class E2NestedLoops {
    public static void main(String[] args) {

        int y=1;
        while(y<=3){
            int x=1;
            while(x<=5){
                System.out.print("#");
                x++;
            }
            System.out.println();
            y++;
        }
    }
}
