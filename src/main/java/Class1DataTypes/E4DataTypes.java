package Class1DataTypes;

public class E4DataTypes {
    public static void main(String[] args) {
        //byte can store numbers up to -128 to 127
        byte age=127;
        //short can store larger numbers from -32768 to 32768
        short iphonePrice=1299; //this is called camelCase
        int bankBalance=32099900; // most widely used
        //With Long, we should add an "L" at the end
        long distance=1280000000000000L;
        System.out.println(age);
        System.out.println(iphonePrice);
        System.out.println(bankBalance);
        System.out.println(distance);
    }
}
