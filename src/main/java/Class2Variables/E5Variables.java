package Class2Variables;

public class E5Variables {
    public static void main(String[] args) {
        String firstName = "Alex";
        String lastName = "Siva";
        String city ="Ho Chi Minh";
        String state = "Saigon";
        String phoneNumber = "+900-458455454";
        System.out.println("My First name is " +firstName+ " and my last name is "+lastName);
        System.out.println("I live in city "+city+" and state "+state);
        System.out.println("My phone Number is "+phoneNumber);
        System.out.println();
        city = "China";
        state = "Beingin";
        phoneNumber = "+26734-95845465665";
        System.out.println("My name is "+firstName+" "+lastName+ " and i moved to new city "+city+ " and my new state is "+state);
        System.out.println("My phone number is "+phoneNumber);
    }
}
