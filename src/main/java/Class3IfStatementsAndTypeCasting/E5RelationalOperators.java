package Class3IfStatementsAndTypeCasting;
//This is a way of comparing one or more things
public class E5RelationalOperators {
    public static void main(String[] args) {
        //Example
        System.out.println(90>982);// is 90 greater than 982
        System.out.println(10<15);// is 10 less than 15
        System.out.println(40==90);//is 40 equal to 90
        System.out.println(10!=15);// is 10 not equal to 15
        System.out.println(10>=15);// is 10 greater than or equal to 15
        System.out.println(10<=10);// is 10 less than or equal to 15

    }
}
