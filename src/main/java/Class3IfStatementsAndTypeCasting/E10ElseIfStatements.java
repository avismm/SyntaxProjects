//Else if statements

public class E10ElseIfStatements {
    public static void main(String[] args) {
       int day=9;
      if (day==1){
          System.out.println("it is Mon");
      }else if(day==2){
          System.out.println("It is Tue. Let us go to work");
      }else if(day==3) {
          System.out.println("It is wed. Let us go shopping");
      }else if(day==4) {
          System.out.println("It is Thur. Let us go to sch");
      }else if(day==5) {
          System.out.println("It is Fri. Let us go to party");
      }else{
          System.out.println("wrong day");
      }

    }
}
