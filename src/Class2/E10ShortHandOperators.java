package Class2;

public class E10ShortHandOperators {
    public static void main(String[] args) {
        int number=50;
        number=number+10;
        number+=40;
        number-=5;
        number*=8;
        /* this operation takes the previous value and adds 10 to it
        and then stores the results back to the variable "number"
        */
        System.out.println(number);

    }
}
