package Class2;

public class T10ArithmeticTask {
    public static void main(String[] args) {
        double num1= 5;
        double num2=9;
        double num3=num1+num2;
        double num4=num1-num2;
        double num5=num1*num2;
        double num6=num1/num2;

        System.out.println("The addition of 2 numbers "+num2+ " and "+num1+ " is equal to "+ num3);
        System.out.println("The subtraction of 2 numbers "+num2+ " and "+num1+ " is equal to "+ num4);
        System.out.println("The division of 2 numbers "+num2+ " and "+num1+ " is equal to "+ num5);
        System.out.println("The multiplication of 2 numbers "+num2+ " and "+num1+ " is equal to "+num6);




    }
}
